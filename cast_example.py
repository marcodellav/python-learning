# we initially define the 2 variables as strings so the sum of the two is a concatenated string

num_1 = '100'
num_2 = '200'

conc_str = (num_1 + num_2)

print (conc_str)


# casting the num_1 and num_2 as int so the sum is a number

num_1 = int(num_1)
num_2 = int(num_2)

sum_int = (num_1 + num_2)

print (sum_int)
